-- Prueba Triggers
CREATE TABLE MOVIMIENTOS (
    pieza NUMBER,
    fecha DATE,
    almacen VARCHAR2(15),
    constraint movimientos_pk PRIMARY KEY (pieza, fecha)
);

CREATE TABLE COPIA_MOVIMIENTOS (
    pieza NUMBER,
    fecha DATE,
    almacen VARCHAR2(15)
);

CREATE VIEW MOVIMIENTOS_HOY AS SELECT * FROM MOVIMIENTOS WHERE fecha = SYSDATE;

-- Ejercicio 1
CREATE OR REPLACE TRIGGER TR_COPIA_MOV
AFTER INSERT OR DELETE OR UPDATE ON MOVIMIENTOS FOR EACH ROW
BEGIN
    IF INSERTING THEN
        INSERT INTO COPIA_MOVIMIENTOS VALUES(:new.pieza, :new.fecha, :new.almacen);
    ELSIF DELETING THEN
        DELETE FROM COPIA_MOVIMIENTOS WHERE pieza = :old.pieza AND fecha = :old.fecha AND almacen = :old.almacen;
    ELSIF UPDATING THEN
        UPDATE COPIA_MOVIMIENTOS SET pieza = :new.pieza, fecha = :new.fecha, almacen = :new.almacen
            WHERE pieza = :old.pieza AND fecha = :old.fecha AND almacen = :old.almacen;
    END IF;
END;
/

