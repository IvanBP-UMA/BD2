-- Prueba PL/SQL
CREATE TABLE INFO_TRIGGERS (
    NOMBRE VARCHAR2(128) PRIMARY KEY,
    TABLA VARCHAR2(128),
    VALIDO CHAR(1) CHECK(VALIDO IN ('S', 'N'))
);

CREATE TABLE INFO_TABLAS (
    NOMBRE VARCHAR2(128) PRIMARY KEY
);

CREATE OR REPLACE PACKAGE PKG_MANIPULA AS
    PROCEDURE PR_INSERTA_TRIGGER(P_NOMBRE IN VARCHAR2, P_TABLA IN VARCHAR2, P_VALIDO IN VARCHAR2);
    PROCEDURE PR_INSERTA_TRIGGER_2(P_NOMBRE IN VARCHAR2, P_TABLA IN VARCHAR2, P_VALIDO IN VARCHAR2);
END;
/

CREATE OR REPLACE PACKAGE BODY PKG_MANIPULA AS
    PROCEDURE PR_INSERTA_TRIGGER(P_NOMBRE IN VARCHAR2, P_TABLA IN VARCHAR2, P_VALIDO IN VARCHAR2) IS
    BEGIN
        INSERT INTO INFO_TRIGGERS VALUES (P_NOMBRE, P_TABLA, P_VALIDO);
        COMMIT;
    END;
    
    PROCEDURE PR_INSERTA_TRIGGER_2(P_NOMBRE IN VARCHAR2, P_TABLA IN VARCHAR2, P_VALIDO IN VARCHAR2) IS
    parent_key_not_found EXCEPTION;
    PRAGMA EXCEPTION_INIT(parent_key_not_found, -02291);
    BEGIN
        INSERT INTO INFO_TRIGGERS VALUES (P_NOMBRE, P_TABLA, P_VALIDO);
        COMMIT;
    EXCEPTION
        WHEN parent_key_not_found THEN
            INSERT INTO INFO_TABLAS VALUES (P_TABLA);
            PR_INSERTA_TRIGGER(P_NOMBRE, P_TABLA, P_VALIDO);
    END;
END;
/

