CREATE OR REPLACE TRIGGER tr_baja
AFTER DELETE OR UPDATE ON USUARIO FOR EACH ROW
BEGIN
    INSERT INTO HISTORIAL VALUES (:old.nombre, :old.clave, SYSDATE);
END;
