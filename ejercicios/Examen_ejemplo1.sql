-- Ejercicio 2
CREATE OR REPLACE FUNCTION CLAVE_VULNERABLE (p_nombre IN VARCHAR2)
RETURN BOOLEAN IS
v_count INTEGER;
CURSOR c_claves IS SELECT u.clave og, h.clave ant FROM USUARIO u, HISTORIAL h WHERE u.nombre = h.nombre AND u.nombre = p_nombre;
BEGIN
    SELECT COUNT(*) INTO v_count FROM HISTORIAL WHERE nombre = p_nombre;
    IF v_count = 0 THEN 
        RAISE_APPLICATION_ERROR(-20001, 'USUARIO_INICIAL');
    END IF;
    
    FOR v_clave IN c_claves LOOP 
        IF v_clave.og = v_clave.ant THEN
            RETURN TRUE;
        END IF;
    END LOOP;
    RETURN FALSE;
END;
/

BEGIN
    IF CLAVE_VULNERABLE('MANUEL') THEN
        DBMS_OUTPUT.PUT_LINE('TRUE');
    ELSE
        DBMS_OUTPUT.PUT_LINE('FALSE');
    END IF;
END;
/

-- Ejercicio 3
CREATE OR REPLACE PROCEDURE DETECTA_VULNERABLE IS
no_user EXCEPTION;
PRAGMA EXCEPTION_INIT(no_user, -20001);
CURSOR c_usuarios IS SELECT nombre FROM USUARIO;
BEGIN
    DELETE FROM VULNERABLE;
    FOR v_usuario IN c_usuarios LOOP
        BEGIN 
            IF CLAVE_VULNERABLE(v_usuario.nombre) THEN
                INSERT INTO VULNERABLE VALUES (v_usuario.nombre);
            END IF;
        EXCEPTION
            WHEN no_user THEN
                NULL;
        END;
    END LOOP;
END;
/

EXEC DETECTA_VULNERABLE();

