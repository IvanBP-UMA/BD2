CREATE TABLE USUARIO (
    NOMBRE VARCHAR2(16) PRIMARY KEY,
    CLAVE VARCHAR2(16)
);

CREATE TABLE HISTORIAL (
    NOMBRE VARCHAR2(16),
    CLAVE VARCHAR2(16),
    BAJA DATE
);

CREATE TABLE VULNERABLE (
    NOMBRE VARCHAR2(16),
    FOREIGN KEY (NOMBRE) REFERENCES USUARIO(NOMBRE)
);

